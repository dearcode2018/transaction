package com.hua.entity;

/**
 * 描述: 简单对象
 * @author qye.zheng
 * 
 * SimpleObj
 */
public final class SimpleObj
{

	private Long oid;
	
	private String name;

	/**
	 * @return the oid
	 */
	public Long getOid()
	{
		return oid;
	}

	/**
	 * @param oid the oid to set
	 */
	public void setOid(Long oid)
	{
		this.oid = oid;
	}

	/**
	 * @return the name
	 */
	public String getName()
	{
		return name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(String name)
	{
		this.name = name;
	}
	
}
